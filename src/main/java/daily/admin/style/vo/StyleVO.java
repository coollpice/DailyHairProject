package daily.admin.style.vo;

public class StyleVO {
	private int styling_num;
	private int des_num;
	private String styling_name;
	private int styling_price;
	private int styling_option;
	private String styling_regdate;
	
	
	public int getStyling_num() {
		return styling_num;
	}
	public void setStyling_num(int styling_num) {
		this.styling_num = styling_num;
	}
	public int getDes_num() {
		return des_num;
	}
	public void setDes_num(int des_num) {
		this.des_num = des_num;
	}
	public String getStyling_name() {
		return styling_name;
	}
	public void setStyling_name(String styling_name) {
		this.styling_name = styling_name;
	}
	public int getStyling_price() {
		return styling_price;
	}
	public void setStyling_price(int styling_price) {
		this.styling_price = styling_price;
	}
	public int getStyling_option() {
		return styling_option;
	}
	public void setStyling_option(int styling_option) {
		this.styling_option = styling_option;
	}
	public String getStyling_regdate() {
		return styling_regdate;
	}
	public void setStyling_regdate(String styling_regdate) {
		this.styling_regdate = styling_regdate;
	}
	
	
	
}
