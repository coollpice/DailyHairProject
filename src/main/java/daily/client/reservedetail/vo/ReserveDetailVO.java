package daily.client.reservedetail.vo;

public class ReserveDetailVO {
	private int detail_num;
	private int rest_num;
	private int styling_num;

	public int getDetail_num() {
		return detail_num;
	}

	public int getRest_num() {
		return rest_num;
	}

	public int getStyling_num() {
		return styling_num;
	}

	public void setDetail_num(int detail_num) {
		this.detail_num = detail_num;
	}

	public void setRest_num(int rest_num) {
		this.rest_num = rest_num;
	}

	public void setStyling_num(int styling_num) {
		this.styling_num = styling_num;
	}
}
